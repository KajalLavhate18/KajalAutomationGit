package Array_Study;

public class SingleDimensionArray {

	public static void main(String[] args) {
		//String
		
		//name0=velocity 
		//name1=corporate
		//name2=training
		//name3=centre
		
		String name[]=new String[4];
		name[0]="velocity";
		name[1]="corporate";
		name[2]="training";
		name[3]="centre";
		System.out.println(name[0]);
		
		//INTSTUDY
		int marks[]=new int[6];
		marks[0]=35;
		marks[1]=60;
		marks[2]=65;
		marks[3]=70;
		marks[4]=72;
		marks[5]=90;
		System.out.println(marks[4]);
		
		//one line 
		int mark[]= {5,9,5,8,10};
		System.out.println(mark[3]);
		
		//CHARStudy
		
		//grade A,B,C,D,E
		char grade[]=new char[5];
		grade[0]='A';
		grade[1]='B';
		grade[2]='C';
		grade[3]='D';
		grade[4]='E';
		System.out.println(grade[0]);
		
		//boolean
		boolean result[]=new boolean[3];
		result[0]=true;
		result[1]=false;
		result[2]=true;
		System.out.println(result[1]);
		
		
		
		
	}

}
